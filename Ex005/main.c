#include <stdio.h>
#include <stdlib.h>

//Exercício 005: Variáveis float

int main()
{
    //Variáveis do tipo float representam o conjunto dos números reais e podem conter até 6 casas decimais
    float pi = 3.1415; //Atribuindo um valor aproximado da constante pi à uma variável de mesmo nome.
    printf("O valor da constante pi é: %.4f", pi); //A máscara referente a uma variável do tipo float é %f
    return 0;
}
